package org.slc.sli.api.security;

import org.springframework.stereotype.Component;

import org.slc.sli.domain.Entity;

import java.security.Principal;
import java.util.List;

/**
 * Attribute holder for SLI Principal
 * 
 * @author dkornishev
 * @author shalka
 */
@Component
public class SLIPrincipal implements Principal {
    
    private String       id;
    private String       name;
    private String       realm;
    private String       externalId;
    private List<String> roles;

    private Entity       entity;
    
    public SLIPrincipal(String id) {
        this.id = id;
    }

    public SLIPrincipal() {

    }

    @Override
    public String getName() {
        return this.name;
    }
    
    public String getId() {
        return id;
    }
    
    /**
     * Gets the fully-qualified LDAP string generated by OpenAM.
     * 
     * @return String containing Realm information.
     */
    public String getRealm() {
        return realm;
    }
    
    public String getExternalId() {
        return externalId;
    }
    
    public void setId(String id) {
        this.id = id;
    }
    
    public void setName(String name) {
        this.name = name;
    }
    
    public void setRealm(String realm) {
        this.realm = realm;
    }
    
    public void setExternalId(String externalId) {
        this.externalId = externalId;
    }
    
    public void setEntity(Entity entity) {
        this.entity = entity;
    }
    
    public Entity getEntity() {
        return entity;
    }
    
    public List<String> getRoles() {
        return roles;
    }
    
    public void setRoles(List<String> roles) {
        this.roles = roles;
    }


}
